// Q1: Store three integers in x, y and z. Print their sum.

void main() {
  // Variable decleration
  int x, y, z;

  // Assigning value
  x = 12;
  y = 8;
  z = 10;

  // Initilazing 'sum' variable with sum of x, y and z
  int sum = x + y + z;

  // Printing sum
  print("After adding ${x}, ${y} and ${z} the result is: ${sum}");
}
